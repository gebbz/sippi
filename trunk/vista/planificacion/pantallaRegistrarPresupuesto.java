    public void MostrarMensaje(String cod)
    {
        if(cod.equals("ME-0001"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo cargar el presupuesto","Error en la Carga",JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
        if(cod.equals("ME-0002"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo cargar los datos de la Obra","Error en la Carga",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("ME-0003"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo crear la nueva Etapa","Error en la Carga",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("ME-0004"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo cargar los datos de la etapa seleccionada","Error en la Carga",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("ME-0005"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo modificar la fecha de la Etapa","Error",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("ME-0006"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo modificar la ganancia de la empresa para esta obra","Error",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("ME-0007"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo modificar el porcentaje de consumibles para la obra","Error",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("ME-0008"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo registrar los cambios en los viáticos","Error",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("ME-0009"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo cargar el listado de Tareas","Error",JOptionPane.ERROR_MESSAGE);
        }
    }

