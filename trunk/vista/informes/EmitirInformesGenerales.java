    private void emitirInforme(int informe) {
        
        // Valido los filtros Generales
        if(!validarFiltros()){ return; }
        
        // Inicio el Reporte
        prepararEmision();
        
        // Atencion, lanzar todos los informes como Threads
            switch(informe){
                // ============================================================
                case INFORME_GANANCIAS_EMPRESA:
                        Thread thread = new Thread(new Runnable() {
                            @Override
                            public void run() {
                                try{
                                    GestorInformesGenerales gestor = new GestorInformesGenerales(filtroFechaInicio.getDate(),filtroFechaFin.getDate());
                                    gestor.generarInformeGanancias();
                                    concluirEmision(JOptionPane.INFORMATION_MESSAGE,"<HTML>Se concluyo con <b>éxito</b> la generación del Informe");
                                }catch(Exception e){
                                    System.err.println("ERROR:" + e.getMessage());
                                    concluirEmision(JOptionPane.ERROR_MESSAGE,"<HTML><b>Error al generar el informe:</b>\n"+e.getMessage());
                                }
                            }
                        });
                        thread.start();
                    break;
                // ============================================================
                case INFORME_COTIZACIONES_RECHAZADAS:
                        Thread thread2 = new Thread(new Runnable() {
                            @Override
                            public void run() {
                                try{
                                    GestorInformesGenerales gestor = new GestorInformesGenerales(filtroFechaInicio.getDate(),filtroFechaFin.getDate());
                                    gestor.generarInformeCantidadCotizacionesRechazadas();
                                    concluirEmision(JOptionPane.INFORMATION_MESSAGE,"<HTML>Se concluyo con <b>éxito</b> la generación del Informe");
                                }catch(Exception e){
                                    System.err.println("ERROR:" + e.getMessage());
                                    concluirEmision(JOptionPane.ERROR_MESSAGE,"<HTML><b>Error al generar el informe:</b>\n"+e.getMessage());
                                }
                            }
                        });
                        thread2.start();
                    break;                    
                    
                 case INFORME_RANKING_PROVEEDORES:
                        Thread thread3 = new Thread(new Runnable() {
                            @Override
                            public void run() {
                                try{
                                    String filtroProveedor;
                                    if(filtroProveedorPorCantidad.isSelected()) {
                                        filtroProveedor = "Por Cantidad";
                                    }
                                    else {
                                        filtroProveedor = "Por Monto";
                                    }
                                    
                                    String filtroProducto;
                                    if(filtroProveedorPorCantidad.isSelected()) {
                                        filtroProducto = "Por Cantidad";
                                    }
                                    else {
                                        filtroProducto = "Por Monto";
                                    }
                                    
                                    GestorReportesCompras gestor = new GestorReportesCompras();
                                    gestor.emitirRankingProveedores(filtroFechaInicio.getDate(),
                                            filtroFechaFin.getDate(),
                                            filtroProveedor,
                                            filtroProducto,
                                            (Integer)spinnerProdXProv.getValue());
                                    concluirEmision(JOptionPane.INFORMATION_MESSAGE,"<HTML>Se concluyo con <b>éxito</b> la generación del Informe");
                                }catch(Exception e){
                                    System.err.println("ERROR:" + e.getMessage());
                                    concluirEmision(JOptionPane.ERROR_MESSAGE,"<HTML><b>Error al generar el informe:</b>\n"+e.getMessage());
                                }
                            }
                        });
                        thread3.start();
                    break;                                         
            }
        
    }

    protected void prepararEmision() {
        pb.setVisible(true);
        bloquearOtrosInformes(true);
    }
    
    protected void concluirEmision(int tipoMensaje,String mensaje) {
        pb.setVisible(false);
        bloquearOtrosInformes(false);
        mostrarMensaje(tipoMensaje,"Atencion!", mensaje);
    }    

    private void initFiltros() {
        initFiltrosGananciasEmpresa();
    }

    private void initFiltrosGananciasEmpresa() {
        
        Date inicio = new Date();
        inicio.setMonth(Calendar.JANUARY);
        inicio.setDate(1);
        Date fin = new Date();
        fin.setMonth(Calendar.DECEMBER);
        fin.setDate(31);
        
        filtroFechaInicio.setDate(inicio);
        filtroFechaFin.setDate(fin);
    }
    
