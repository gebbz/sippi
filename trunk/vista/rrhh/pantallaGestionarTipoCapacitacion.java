    public void llenarTabla()
    {

        Object[] nombreColumnas = {"Nombre","Descripci贸n"};
        DefaultTableModel modelo = new DefaultTableModel();
        for (int i = 0; i < nombreColumnas.length; i++)
        {
           modelo.addColumn(nombreColumnas[i]);
        }
        tablaTipos.setModel(modelo);

        modelo = (DefaultTableModel) tablaTipos.getModel();

        ArrayList<NTupla> lista = gestor.mostrarNombreTiposCapacitacion();
        Iterator it = lista.iterator();

        Object[] fila = new Object[2];

        while (it.hasNext())
        {
            NTupla t = (NTupla)it.next();
            fila[0] = t;
            fila[1] = t.getData();
            modelo.addRow(fila);
        }

    }


