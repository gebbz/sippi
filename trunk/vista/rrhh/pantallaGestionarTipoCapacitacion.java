    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed

     if(!txtNombre.getText().isEmpty())
     {
         if(this.oidEdit == 0)
         {
                TipoCapacitacion nuevo = gestor.crear(txtNombre.getText(),txtDesc.getText());

                if(nuevo!=null)
                {
                    // CARGA CORRECTA
                    txtNombre.setText("");
                    txtDesc.setText("");
                    JOptionPane.showMessageDialog(this,"Carga Exitosa","Se completo correctamente la carga de "+nuevo.getNombre(),JOptionPane.INFORMATION_MESSAGE);
                    llenarTabla();
                }
                else
                {
                    // CARGA INCORRECTA
                    JOptionPane.showMessageDialog(this,"Error","Se produjo un error al intentar crear el Tipo de Capacitaci贸n",JOptionPane.INFORMATION_MESSAGE);
                }
         }
         else
         {
                if(gestor.modificar(oidEdit,txtNombre.getText(),txtDesc.getText()))
                {
                    // CARGA CORRECTA
                    txtNombre.setText("");
                    txtDesc.setText("");
                    this.oidEdit = 0;
                    btnCancelEdit.setVisible(false);
                    btnNuevo.setText("Agregar Nuevo");

                    JOptionPane.showMessageDialog(this,"Modificaci贸n Exitosa","Se completo correctamente la modificaci贸n",JOptionPane.INFORMATION_MESSAGE);
                    llenarTabla();
                }
                else
                {
                    // CARGA INCORRECTA
                    JOptionPane.showMessageDialog(this,"Error","Se produjo un error al modificar el Tipo de Capacitaci贸n",JOptionPane.INFORMATION_MESSAGE);
                }
         }
     }

    }//GEN-LAST:event_btnNuevoActionPerformed

