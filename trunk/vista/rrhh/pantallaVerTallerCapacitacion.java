    public void setEmpleados(ArrayList<NTupla> listaEmpleado) {

        DefaultTableModel modelo = (DefaultTableModel)tblEmpleados.getModel();
        Iterator it = listaEmpleado.iterator();
        while (it.hasNext())
        {
            Object[] fila = new Object[3];
            NTupla nt = (NTupla)it.next();
            fila[0] = nt;
                String[] dato = (String[]) nt.getData();
                fila[1] = dato[0];
                fila[2] = dato[1];
            modelo.addRow(fila);
        }

    }

