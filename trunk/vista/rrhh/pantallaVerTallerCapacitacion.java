    public void setHorarios(ArrayList<NTupla> lista)
    {
        Iterator<NTupla> it = lista.iterator();

        DefaultTableModel modelo = (DefaultTableModel)tblHorarios.getModel();

        while (it.hasNext())
        {
            NTupla nt = it.next();
            Object[] fila = new Object[3];
            fila[0] = nt;

                String[] horas = (String[])nt.getData();
                fila[1] = horas[0];
                fila[2] = horas[1];
                
            modelo.addRow(fila);
        }
    }

