    private void filtrarLista()
    {
            ArrayList<NTupla> lista = new ArrayList<NTupla>();

            if(optTodo.isSelected())
            {
                lista = gestor.buscarTallerCapacitacionPorNombre(txtBuscar.getText());
            }
            if(optAlta.isSelected())
            {
                lista = gestor.buscarTallerCapacitacionPorNombreEnAlta(txtBuscar.getText());
            }
            if(optBaja.isSelected())
            {
                lista = gestor.buscarTallerCapacitacionPorNombreEnBaja(txtBuscar.getText());
            }

            DefaultTableModel modelo = (DefaultTableModel)tblLista.getModel();
            modelo = TablaUtil.vaciarDefaultTableModel(modelo);

            setCantidadResultados(lista.size());

            Iterator<NTupla> it = lista.iterator();
            while (it.hasNext())
            {
                NTupla nt = (NTupla) it.next();

                Object[] fila = new Object[5];
                fila[0] = nt;
                    String[] aux = (String[])nt.getData();
                    fila[1] = aux[0];
                    fila[2] = aux[1];
                    fila[3] = aux[2];
                    fila[4] = aux[3];

                modelo.addRow(fila);
            }
    }

