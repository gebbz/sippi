   /**
     * EL-0016 : No se pudo cargar el listado de Talleres de Capacitación
     * ME-0006 : Se dio de baja el taller
     * ME-0007 : Se dio de alta el taller
     * EG-0018 : No se pudo abrir la conexión con la DB para dar de baja el taller
     * @param cod
     */
    public void MostrarMensaje(String cod)
    {
        System.out.println("Se detecto el mensaje: "+cod);
        if(cod.equals("EG-0016"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se pudo cargar la lista de Talleres","Error en la Carga",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("EG-0018"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"No se abrir la conexión con ls base de datos para dar de baja el Taller","Error en la Conexión",JOptionPane.ERROR_MESSAGE);
        }
        if(cod.equals("ME-0006"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"Se dió de Baja el Taller de Capacitación","Exito",JOptionPane.INFORMATION_MESSAGE);
        }
        if(cod.equals("ME-0007"))
        {
            JOptionPane.showMessageDialog(this.getParent(),"Se dió de Alta el Taller de Capacitación","Exito",JOptionPane.INFORMATION_MESSAGE);
        }
    }

