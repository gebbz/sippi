    private void habilitarVentana() {
        llenarComboHerramientas();
        llenarComboEstadoHerramienta();
    }
    
    private void llenarComboHerramientas() 
    {   
        try 
        {
            DefaultComboBoxModel dcbm = (DefaultComboBoxModel) cmbHerramienta.getModel();
            dcbm.removeAllElements();
            Iterator<Herramienta> iter = HibernateUtil.getSession().createQuery("from Herramienta h order by h.id").iterate();
            while (iter.hasNext())
            {
                Herramienta h = iter.next(); 
                Tupla tp = new Tupla(h.getId(),h.getNombre());
                dcbm.addElement(tp);
            }    
        } 
        catch (Exception ex) 
        {
           ex.printStackTrace();
           JOptionPane.showInternalMessageDialog(this,"Error!","Se produjo un error al cargar las Herramientas",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void llenarComboTipoHerramienta(int idHerramienta)
    {
        try 
        {
            DefaultComboBoxModel dcbm = (DefaultComboBoxModel) cmbTipoHerramienta.getModel();
            dcbm.removeAllElements();
            Herramienta herramienta = (Herramienta) HibernateUtil.getSession().createQuery("from Herramienta h where h.id=:idHerramienta").setParameter("idHerramienta", idHerramienta).uniqueResult();
            Iterator<RecursoEspecifico> iter = (Iterator<RecursoEspecifico>) herramienta.getRecursosEspecificos().iterator();
            while (iter.hasNext())
            {
                RecursoEspecifico re = iter.next(); 
                Tupla tp = new Tupla(re.getId(),re.getNombre());
                dcbm.addElement(tp);
            }    
        } 
        catch (Exception ex) 
        {
           ex.printStackTrace();
           JOptionPane.showInternalMessageDialog(this,"Error!","Se produjo un error al cargar los tipos de Herramienta",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void llenarComboEstadoHerramienta()
    {
        DefaultComboBoxModel dcbm = (DefaultComboBoxModel) cmbEstado.getModel();
        Iterator<String> itEstados = HerramientaDeEmpresa.getEstados().iterator();
        while(itEstados.hasNext())
        {
            String estado = itEstados.next();
            dcbm.addElement(estado);
        }
    }
