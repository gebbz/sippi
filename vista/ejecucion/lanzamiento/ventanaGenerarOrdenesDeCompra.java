    private void initTabla() {
        tablaItemsAComprar.setRowHeight(TABLA_DEFAULT_ALTO);
        DefaultTableModel modelo = (DefaultTableModel) tablaItemsAComprar.getModel();

        TableColumn col = tablaItemsAComprar.getColumnModel().getColumn(TABLA_ITEM_COMPRA_COLUMNA_PROVEEDOR);
        
        Iterator<NTupla> itFilas = gestor.getItemsAComprar().iterator();
        while(itFilas.hasNext())
        {
            NTupla nTupla = itFilas.next();
            ArrayList<Tupla> proveedores = (ArrayList<Tupla>)nTupla.getData();
            tablaItemOrdenesDeCompraCellEditor editor = new tablaItemOrdenesDeCompraCellEditor(tablaItemsAComprar, proveedores);
            col.setCellEditor(editor);
        }
    }
