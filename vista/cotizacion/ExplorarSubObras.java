    private void cerrarVentana()
    {
        int btn = JOptionPane.showConfirmDialog(this,"¬øDesea Guardar los cambios antes de cerrar?","Atenci√≥n",JOptionPane.YES_NO_OPTION);
        switch(btn)
        {
            case JOptionPane.YES_OPTION:
                    if(txtNroCotizacion.getText().equals("P0000-0000000A"))
                    {
                        int n = JOptionPane.showConfirmDialog(this,"El n√∫mero de la cotizaci√≥n es P0000-0000000A ¬øEst√° seguro que no desea cambiarlo? '","Atenci√≥n!",JOptionPane.YES_NO_OPTION);

                        if(n==JOptionPane.NO_OPTION)
                        {
                                guardarCotizacion();
                        }
                    }
                    else
                    {
                        guardarCotizacion();    
                    }
                this.dispose();
                break;
            case JOptionPane.NO_OPTION:
                this.dispose();
                break;
        }

    }

   
