    protected void initVentanaDefecto(String nombre) {
        setNombrePanel(nombre);
        CotizacionDescripcion ecd = new CotizacionDescripcion(gestor.getGestorDescripcion());
        panel.setViewportView(ecd);
        ecd.setVisible(true);
    }

