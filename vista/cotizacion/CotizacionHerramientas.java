    public void llenarComboHerramientas(ArrayList<Tupla> listaTipos) 
    {
        cmbHerramienta.removeAllItems();
        for (int i = 0; i < listaTipos.size(); i++) 
        {
            Tupla tp = listaTipos.get(i);
            cmbHerramienta.addItem(tp);
        }
    }    
    
