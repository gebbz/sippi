    private void cerrarVentana()
    {
        if(necesita_guardar)
        {
            int btn = JOptionPane.showConfirmDialog(this,"¿Está seguro que desea cerrar sin guardar los cambios?","Atención",JOptionPane.YES_NO_OPTION);
            switch(btn)
            {
                case JOptionPane.YES_OPTION:
                    this.dispose();
                    break;
            }
        }
        else
        {
            this.dispose();
        }
    }

