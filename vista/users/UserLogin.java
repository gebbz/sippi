    private void initTable() 
    {
        tablaUsers.setDefaultRenderer(Object.class,new IconCellRenderer());
        
            tablaUsers.setRowHeight(70);
            tablaUsers.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            TableColumn col = tablaUsers.getColumnModel().getColumn(0);
            col.setPreferredWidth(70);
            TableColumn col2 = tablaUsers.getColumnModel().getColumn(1);
            col2.setPreferredWidth(200);

        fillTable();
    }

    private void fillTable() 
    {
        DefaultTableModel modelo = (DefaultTableModel) tablaUsers.getModel();
        
        Object[] fila = new Object[2];
        ImageIcon icon = new ImageIcon(getClass().getResource("/res/retratos/avatar1.jpg"));
        fila[0] = new JLabel(icon);
        fila[1] = "Usuario de Prueba";
        
        Object[] fila2 = new Object[2];
        ImageIcon icon2 = new ImageIcon(getClass().getResource("/res/retratos/avatar2.jpg"));
        fila2[0] = new JLabel(icon2);
        fila2[1] = "Iuga";        
        
        modelo.addRow(fila);
        modelo.addRow(fila2);
    }
